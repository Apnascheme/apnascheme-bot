      message = `‚úÖ ‡§≠‡•Å‡§ó‡§§‡§æ‡§® ‡§∏‡§´‡§≤!\n\n‡§Ü‡§™‡§ï‡•Ä ‡§Ø‡•ã‡§ú‡§®‡§æ‡§è‡§Ç (${eligibleSchemes.length}):\n\n`;
